void TypeWriter(const std::string& text, int delayMs) {
	for (char c : text) {
		std::cout << c << std::flush;
		std::this_thread::sleep_for(std::chrono::milliseconds(delayMs));
	}
}

void ClearScreen() {
	system("cls"); // Windows 기준
}

void SetConsoleColor(int color) {
	SetConsoleTextAttribute(GetStdHandle(STD_OUTPUT_HANDLE), color);
}

void SleepMs(int ms) {
	std::this_thread::sleep_for(std::chrono::milliseconds(ms));
}

void FlashInvertColors(int times, int delayMs, const std::string& art)
{
	HANDLE hConsole = GetStdHandle(STD_OUTPUT_HANDLE);
	CONSOLE_SCREEN_BUFFER_INFO csbi;
	GetConsoleScreenBufferInfo(hConsole, &csbi);
	WORD originalAttrs = csbi.wAttributes;

	for (int i = 0; i < times; ++i) {
		system("cls");  // 화면 지우기
		WORD invertedAttrs = ((originalAttrs & 0x0F) << 4) | ((originalAttrs & 0xF0) >> 4);
		SetConsoleTextAttribute(hConsole, invertedAttrs);
		std::cout << art << std::flush;
		std::this_thread::sleep_for(std::chrono::milliseconds(delayMs));

		system("cls");  // 다시 화면 지우기
		SetConsoleTextAttribute(hConsole, originalAttrs);
		std::cout << art << std::flush;
		std::this_thread::sleep_for(std::chrono::milliseconds(delayMs));
	}

	SetConsoleTextAttribute(hConsole, originalAttrs);
}


